// Задайте массив из вещественных чисел с ненулевой дробной частью. Найдите разницу между максимальным и минимальным элементов массива.

// Начальные условия:

// int[] array = {0.25, 5.4, 1.3, 2.1, 3.8, 5.2, 3.01}; // Создание массива
// Выводится: 5.15

using System;

class Program
{
    static void Main()
    {
        // Создание массива вещественных чисел
        double[] array = { 0.25, 5.4, 1.3, 2.1, 3.8, 5.2, 3.01 };

        // Инициализация переменных для хранения максимального и минимального значений
        double max = array[0];
        double min = array[0];

        // Поиск максимального и минимального значений в массиве
        for (int i = 1; i < array.Length; i++)
        {
            if (array[i] > max)
            {
                max = array[i];
            }
            if (array[i] < min)
            {
                min = array[i];
            }
        }

        // Вычисление разницы между максимальным и минимальным значениями
        double difference = max - min;

        // Вывод результата
        Console.WriteLine(difference); // Ожидаемый вывод: 5.15
    }
}

